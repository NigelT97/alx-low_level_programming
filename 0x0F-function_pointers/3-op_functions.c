#include "3-calc.h"
/**
 * op_add - add two integers
 * @a: integer one
 * @b: integer two
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two integers
 * @a: integer one
 * @b: integer two
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two integers
 * @a: integer one
 * @b: integer two
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two integers
 * @a: integer one
 * @b: integer two
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus of two integers
 * @a: integer one
 * @b: integer two
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
